export const getPlanBySlug = (slug) => READING_PLAN.find(p => p.slug === slug);
export const getPlanByDay = (day) => READING_PLAN.find(p => p.day === day);
export const findDayByBookChapter = (book, chapter) => {
    const chapterNum = parseInt(chapter);
    return READING_PLAN.find(day => {
        return day.passages.some(p => {
            if (p.book !== book) return false;
            if (p.chapters.includes('-')) {
                const [start, end] = p.chapters.split('-').map(Number);
                return chapterNum >= start && chapterNum <= end;
            }
            return parseInt(p.chapters) === chapterNum;
        });
    });
};
